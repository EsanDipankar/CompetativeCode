import java.util.*;
public class Day7Q7 {
    public static void main(String args[]){
        Scanner sc= new Scanner(System.in);
        int n= sc.nextInt();
        for(int j=0;j<n;j++){
            System.out.print("*");
            for(int i=0;i<n-2;i++){
                System.out.print(" ");
            }
        System.out.print("*");
        System.out.println();
        }
        
    }
    
}
